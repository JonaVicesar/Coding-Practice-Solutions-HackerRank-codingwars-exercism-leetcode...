package hackerrank;
import java.util.*;

public class JavaEOF {
	    public static void main(String[] args) {
	        Scanner input = new Scanner(System.in);
	        int counter = 1;
	        while(input.hasNext()){
	            System.out.println(counter + " " + input.nextLine());
	            counter += 1;
	        }
	        
	    }
}

